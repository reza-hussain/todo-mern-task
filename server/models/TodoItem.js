const mongoose = require("mongoose");

const todoItemSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    completed: {
      type: Boolean,
      default: false,
    },
    user: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  {
    timestamps: true,
  }
);
const TodoItem = mongoose.model("TodoItem", todoItemSchema);
module.exports = TodoItem;
